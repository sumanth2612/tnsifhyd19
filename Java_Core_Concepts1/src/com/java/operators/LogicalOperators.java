package com.java.operators;
import java.io.*;

public class LogicalOperators {

	public static void main (String[] args) {
	     
		boolean x = true;
	    boolean y = false;
	      
	    //logical operatora in Java: &&, ||, !
	    System.out.println("x && y: " + (x && y));
	    System.out.println("x || y: " + (x || y));
	    System.out.println("!x: " + (!x));
	    
	}
}

