package com.java.operators;
import java.io.*;

public class ShiftOperators {
	
	public static void main(String[] args)
	{
		int a = 10;
	    
	    // Using left shift
	    System.out.println("a<<1 : " + (a << 1));
	      
	    // Using right shift
	    System.out.println("a>>1 : " + (a >> 1));
	   
	}
}
